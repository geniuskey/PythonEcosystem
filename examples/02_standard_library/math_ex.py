import math

# 반지름이 r인 원의 넓이 계산
r = 5
area = math.pi * r ** 2
print("Area of the circle:", area)

# 로그 계산
print("Natural logarithm of 10:", math.log(10))
print("Base-10 logarithm of 10:", math.log10(10))

# 삼각 함수 사용
angle = 45
# 각도를 라디안으로 변환
radian = math.radians(angle)
print("Sine of 45 degrees:", math.sin(radian))
